#ifndef LOCALMAPPING_H
#define LOCALMAPPING_H

#include "KeyFrame.h"
#include "Map.h"
#include "LoopClosing.h"
#include "Tracking.h"
#include "KeyFrameDatabase.h"

#include <mutex>


namespace CORB_SLAM2
{

class Tracking;
class LoopClosing;
class Map;
class MapMerging;

class LocalMapping
{
public:
    LocalMapping(Map* pMap, const float bMonocular, int nRobotId);


    long unsigned int GetNextFrameId(int nRobotId);
    long unsigned int GetNextKeyFrameId(int nRobotId);
    long unsigned int GetNextMapPointId();

    void ChangeMap(Map *pMap);

    void SetMapMerging(MapMerging* pMapMerging);

    void SetKeyFrameDatabase(KeyFrameDatabase* pKeyFrameDB);

    void SetLoopCloser(LoopClosing* pLoopCloser);

    void SetTracker(Tracking* pTracker);

    int GetRobotId();

    // Main function
    void Run();

    void InsertKeyFrame(KeyFrame* pKF);

    // Thread Synch
    void RequestStop();
    void RequestReset();
    bool Stop();
    void Release();
    bool isStopped();
    bool stopRequested();
    bool AcceptKeyFrames();
    void SetAcceptKeyFrames(bool flag);
    bool SetNotStop(bool flag);

    void InterruptBA();

    void RequestFinish();
    bool isFinished();

    int KeyframesInQueue(){
        unique_lock<std::mutex> lock(mMutexNewKFs);
        return mlNewKeyFrames.size();
    }

protected:

    bool CheckNewKeyFrames();
    void ProcessNewKeyFrame();
    void CreateNewMapPoints();

    void MapPointCulling();
    void SearchInNeighbors();

    void KeyFrameCulling();

    cv::Mat ComputeF12(KeyFrame* &pKF1, KeyFrame* &pKF2);

    cv::Mat SkewSymmetricMatrix(const cv::Mat &v);

    bool mbMonocular;

    void ResetIfRequested();
    bool mbResetRequested;
    std::mutex mMutexReset;

    bool CheckFinish();
    void SetFinish();
    bool mbFinishRequested;
    bool mbFinished;
    std::mutex mMutexFinish;



    Map* mpMap;

    LoopClosing* mpLoopCloser;
    Tracking* mpTracker;

    std::list<KeyFrame*> mlNewKeyFrames;

    KeyFrame* mpCurrentKeyFrame;

    std::list<MapPoint*> mlpRecentAddedMapPoints;

    std::mutex mMutexNewKFs;

    bool mbAbortBA;

    bool mbStopped;
    bool mbStopRequested;
    bool mbNotStop;
    std::mutex mMutexStop;

    bool mbAcceptKeyFrames;
    std::mutex mMutexAccept;

    int mnRobotId;

    std::mutex mMutexFrameId;
    std::mutex mMutexKeyFrameId;
    std::mutex mMutexMapPointId;

    long unsigned int mnNextFrameId ;
    long unsigned int mnNextKeyFrameId;
    long unsigned int mnNextMapPointId;

    MapMerging *mpMapMerger;
    KeyFrameDatabase *mpKeyFrameDB;
};

} //namespace ORB_SLAM

#endif // LOCALMAPPING_H
